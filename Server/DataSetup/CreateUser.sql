    CREATE USER <ユーザー名> IDENTIFIED BY <パスワード>
      DEFAULT TABLESPACE <データ領域>
      TEMPORARY TABLESPACE <テンポラリ領域>
      PROFILE DEFAULT;
    GRANT <権限、ロール> TO <ユーザー名>;

