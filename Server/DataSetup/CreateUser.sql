spool CreateUser.log

CREATE USER ikou IDENTIFIED BY ikou1
  DEFAULT TABLESPACE MRMS_DATA
  TEMPORARY TABLESPACE MRMS_TEMP
  PROFILE DEFAULT;

GRANT RECOVERY_CATALOG_OWNER TO IKOU;
GRANT RESOURCE TO IKOU;
GRANT SELECT ANY DICTIONARY TO IKOU;
GRANT DBA TO IKOU;
GRANT SELECT ANY TABLE TO IKOU;
GRANT SELECT ANY TABLE,UPDATE ANY TABLE,DELETE ANY TABLE,INSERT ANY TABLE TO IKOU;
GRANT EXECUTE ANY PROCEDURE to IKOU;
GRANT DROP ANY PROCEDURE to IKOU;
GRANT CREATE ANY PROCEDURE to IKOU;
spool off
QUIT;
